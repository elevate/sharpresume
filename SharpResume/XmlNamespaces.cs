namespace Just3Ws.SharpResume
{
  /// <summary>
  /// Xml namespaces used by the #Resume library.
  /// </summary>
  public static class XmlNamespaces
  {
    /// <summary>
    /// The xmlns used for validation of documents.
    /// </summary>
    public const string HRXmlNamespace25 = "http://ns.hr-xml.org/2007-04-15";

    /// <summary>
    /// The schema to validate resume documents.
    /// </summary>
    public const string HRXmlResumeSchemaLocation25 = "http://ns.hr-xml.org/2007-04-15 Resume.xsd";

    /// <summary>
    /// The xml namespace.
    /// </summary>
    public const string Xml = "http://www.w3.org/XML/1998/namespace";

    /// <summary>
    /// The xsi namespace address.
    /// </summary>
    public const string XmlSchemaInstance = "http://www.w3.org/2001/XMLSchema-instance";
  }
}